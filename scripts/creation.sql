-- Create Schema
CREATE SCHEMA IF NOT EXISTS taylorswift;

-- Create tables

CREATE TABLE IF NOT EXISTS taylorswift.Albums (
album_id INT PRIMARY KEY,
album_name VARCHAR(256) NOT NULL,
release_year INT NOT NULL,
remake_year INT,
genre VARCHAR(256),
taylors_version BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS taylorswift.Songs (
song_id INT PRIMARY KEY,
song_name VARCHAR(256) NOT NULL,
duration TIME NOT NULL,
from_the_vault BOOLEAN NOT NULL,
album_id INT REFERENCES Albums(album_id)
);

CREATE TABLE IF NOT EXISTS taylorswift.AlbumAwards (
award_id INT PRIMARY KEY,
award_name VARCHAR(256) NOT NULL,
year_received INT NOT NULL,
album_id INT REFERENCES Albums(album_id)
);

CREATE TABLE IF NOT EXISTS taylorswift.SongAwards (
award_id INT PRIMARY KEY,
award_name VARCHAR(256) NOT NULL,
year_received INT NOT NULL,
song_id INT REFERENCES Songs(song_id)
);

CREATE TABLE IF NOT EXISTS taylorswift.Authors (
author_id INT PRIMARY KEY,
author_name VARCHAR(256) NOT NULL
);

CREATE TABLE IF NOT EXISTS taylorswift.SongAuthors (
song_author_id INT PRIMARY KEY,
song_id INT REFERENCES Songs(song_id),
author_name VARCHAR(256) NOT NULL
);

CREATE TABLE IF NOT EXISTS taylorswift.Tours (
tour_id INT PRIMARY KEY,
tour_name VARCHAR(256) NOT NULL,
start_date DATE,
end_date DATE,
show_count INT
);

CREATE TABLE IF NOT EXISTS  taylorswift.TourSongs (
tour_song_id INT PRIMARY KEY,
tour_id INT REFERENCES Tours(tour_id),
album_id INT REFERENCES Albums(album_id)
);

